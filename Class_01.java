package assignment_10;

public class Class_01 {


    // private variables
    private int age;
    private String name;

    // initialize age
    public void setAge(int age) {
        this.age = age;
    }

    // initialize name
    public void setName(String name) {
        this.name = name;
    }

    // access age
    public int getAge() {
        return this.age;
    }

    // access name
    public String getName() {
        return this.name;
    }
}
